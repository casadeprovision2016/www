import { Router } from 'express';
import { authenticateToken, requireMemberOrAbove, requireLeaderOrAdmin } from '../middleware/auth';
import { validateAndSanitize, schemas } from '../middleware/validation';
import {
  getMinistries,
  getMinistryById,
  createMinistry,
  updateMinistry,
  deleteMinistry,
  getMinistryMembers,
  addMinistryMember,
  removeMinistryMember,
  updateMinistryMember
} from '../controllers/ministriesController';

const router = Router();

// Debug endpoint without auth (DEVELOPMENT ONLY) - Must be before auth middleware
router.get('/debug', async (req, res) => {
  try {
    const { createClient } = require('@supabase/supabase-js');
    const supabase = createClient(
      process.env.SUPABASE_URL!,
      process.env.SUPABASE_SERVICE_ROLE_KEY!
    );
    
    console.log('üîç DEBUG: Starting ministries debug endpoint...');
    const { data, error } = await supabase.from('ministries').select('*').limit(5);
    
    console.log('üîç DEBUG: Query result - Error:', error);
    console.log('üîç DEBUG: Query result - Data count:', data?.length);
    
    return res.json({
      success: true,
      debug: true,
      error: error,
      dataCount: data?.length,
      sampleData: data?.slice(0, 2)
    });
  } catch (err: any) {
    console.error('‚ùå DEBUG ERROR:', err);
    return res.json({
      success: false,
      debug: true,
      error: err.message,
      stack: err.stack
    });
  }
});

// Todas as rotas requerem autentica√ß√£o
router.use(authenticateToken);

// Test endpoint (must come before /:id)
router.get('/test', (req, res) => {
  res.json({ success: true, message: 'Ministries route working' });
});

// Listar minist√©rios
router.get('/', 
  requireMemberOrAbove,
  getMinistries
);

// Buscar minist√©rio por ID (must come after specific routes)
router.get('/:id', requireMemberOrAbove, getMinistryById);

// Buscar membros de um minist√©rio
router.get('/:id/members',
  requireMemberOrAbove,
  validateAndSanitize(schemas.pagination),
  getMinistryMembers
);

// Criar minist√©rio (leader ou admin)
router.post('/',
  requireLeaderOrAdmin,
  validateAndSanitize(schemas.createMinistry),
  createMinistry
);

// Atualizar minist√©rio (leader ou admin)
router.put('/:id',
  requireLeaderOrAdmin,
  updateMinistry
);

// Deletar minist√©rio (leader ou admin)
router.delete('/:id',
  requireLeaderOrAdmin,
  deleteMinistry
);

// Adicionar membro ao minist√©rio (leader ou admin)
router.post('/members',
  requireLeaderOrAdmin,
  addMinistryMember
);

// Atualizar membro do minist√©rio (leader ou admin)
router.put('/members/:id',
  requireLeaderOrAdmin,
  updateMinistryMember
);

// Remover membro do minist√©rio (leader ou admin)
router.delete('/members/:id',
  requireLeaderOrAdmin,
  removeMinistryMember
);

export default router;